import { renderFirstTimeRegistration } from "./render-first-time-registration.js";
import { renderRego } from "./render-rego.js";
import { renderLogin } from "./render-login.js";
import { renderProfileEdit } from "./render-profile-edit.js";
import { renderHome } from "./home-startup.js";
import { renderProjectEdit } from "./render-project-edit.js";
import { renderSearch } from "./render-repo-search.js";
import { renderLanding } from "./render-landing.js";

export const page = {
  FirstTimeRegistration: 1,
  Login: 2,
  ProfileEdit: 3,
  Home: 4,
  Rego: 5, 
  ProjectEdit: 6,
  SearchRepos:7
};
export function whichPageToShow(thePageToShow, gitHubName) {
  switch (thePageToShow) {
    case page.FirstTimeRegistration:
      renderRego(); // The landing page when the user first registers
      break;
    case page.Rego:
      renderRego();
      break;
    case page.Login:
      renderLogin();
      break;
    case page.ProfileEdit:
      renderProfileEdit();
      break;
    case page.Home:
      renderHome();
      break;
    case page.ProjectEdit:
      renderProjectEdit();
          break;
      case page.Landing:
          renderLanding();
          break;
    default:
  }
}
